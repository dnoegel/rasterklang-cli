package c64

// Maps opcodes to short mnemonic labels for diagnostics.

func Mnemonic(op byte) string {
	switch op {
	case 0x00:
		return "BRK"
	case 0x20:
		return "JSR"
	case 0x40:
		return "RTI"
	case 0x4c, 0x6c:
		return "JMP"
	case 0x60:
		return "RTS"
	case 0x69, 0x65, 0x75, 0x6d, 0x7d, 0x79, 0x61, 0x71:
		return "ADC"
	case 0xe9, 0xeb, 0xe5, 0xf5, 0xed, 0xfd, 0xf9, 0xe1, 0xf1:
		return "SBC"
	case 0x29, 0x25, 0x35, 0x2d, 0x3d, 0x39, 0x21, 0x31:
		return "AND"
	case 0x09, 0x05, 0x15, 0x0d, 0x1d, 0x19, 0x01, 0x11:
		return "ORA"
	case 0x49, 0x45, 0x55, 0x4d, 0x5d, 0x59, 0x41, 0x51:
		return "EOR"
	case 0xa9, 0xa5, 0xb5, 0xad, 0xbd, 0xb9, 0xa1, 0xb1:
		return "LDA"
	case 0xa2, 0xa6, 0xb6, 0xae, 0xbe:
		return "LDX"
	case 0xa0, 0xa4, 0xb4, 0xac, 0xbc:
		return "LDY"
	case 0x85, 0x95, 0x8d, 0x9d, 0x99, 0x81, 0x91:
		return "STA"
	case 0x86, 0x96, 0x8e:
		return "STX"
	case 0x84, 0x94, 0x8c:
		return "STY"
	case 0xaa:
		return "TAX"
	case 0xa8:
		return "TAY"
	case 0x8a:
		return "TXA"
	case 0x98:
		return "TYA"
	case 0xba:
		return "TSX"
	case 0x9a:
		return "TXS"
	case 0xe8:
		return "INX"
	case 0xc8:
		return "INY"
	case 0xca:
		return "DEX"
	case 0x88:
		return "DEY"
	case 0xe6, 0xf6, 0xee, 0xfe:
		return "INC"
	case 0xc6, 0xd6, 0xce, 0xde:
		return "DEC"
	case 0xc9, 0xc5, 0xd5, 0xcd, 0xdd, 0xd9, 0xc1, 0xd1:
		return "CMP"
	case 0xe0, 0xe4, 0xec:
		return "CPX"
	case 0xc0, 0xc4, 0xcc:
		return "CPY"
	case 0x24, 0x2c:
		return "BIT"
	case 0x0a, 0x06, 0x16, 0x0e, 0x1e:
		return "ASL"
	case 0x4a, 0x46, 0x56, 0x4e, 0x5e:
		return "LSR"
	case 0x2a, 0x26, 0x36, 0x2e, 0x3e:
		return "ROL"
	case 0x6a, 0x66, 0x76, 0x6e, 0x7e:
		return "ROR"
	case 0x90:
		return "BCC"
	case 0xb0:
		return "BCS"
	case 0xf0:
		return "BEQ"
	case 0x30:
		return "BMI"
	case 0xd0:
		return "BNE"
	case 0x10:
		return "BPL"
	case 0x50:
		return "BVC"
	case 0x70:
		return "BVS"
	case 0x18:
		return "CLC"
	case 0xd8:
		return "CLD"
	case 0x58:
		return "CLI"
	case 0xb8:
		return "CLV"
	case 0x38:
		return "SEC"
	case 0xf8:
		return "SED"
	case 0x78:
		return "SEI"
	case 0x48:
		return "PHA"
	case 0x08:
		return "PHP"
	case 0x68:
		return "PLA"
	case 0x28:
		return "PLP"
	case 0xea, 0x1a, 0x3a, 0x5a, 0x7a, 0xda, 0xfa, 0x80, 0x82, 0x89, 0xc2, 0xe2, 0x04, 0x44, 0x64, 0x14, 0x34, 0x54, 0x74, 0xd4, 0xf4, 0x0c, 0x1c, 0x3c, 0x5c, 0x7c, 0xdc, 0xfc:
		return "NOP"
	case 0x02, 0x12, 0x22, 0x32, 0x42, 0x52, 0x62, 0x72, 0x92, 0xb2, 0xd2, 0xf2:
		return "KIL"
	default:
		return "OP"
	}
}
